package WorkingWithAbstractionLab.P02PointInRectangle;

public class Point {
    private int pointX;
    private int pointY;

    public Point(int pointX, int pointY) {
        this.pointX = pointX;
        this.pointY = pointY;
    }
    public int getPointX(){
        return pointX;
    }

    public int getPointY() {
        return pointY;
    }
}
